private void loadViolationsToTable() {
DefaultTableModel model = new DefaultTableModel(
    new String[]{"Violation ID", "Student No.", "Course", "Year", "Section", "Type", "Description", "Date", "Status"}, 0
);

try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/mariavhea", "root", "");
     Statement stmt = conn.createStatement();
     ResultSet rs = stmt.executeQuery(
         "SELECT v.violation_id, s.student_number, s.course, s.year_level, s.section, " +
         "v.violation_type, v.description, v.date_reported, v.status " +
         "FROM violations v JOIN students s ON v.student_id = s.student_id"
     )) {

    while (rs.next()) {
        model.addRow(new Object[]{
            rs.getInt("violation_id"),
            rs.getString("student_number"),
            rs.getString("course"),
            rs.getInt("year_level"),
            rs.getString("section"),
            rs.getString("violation_type"),
            rs.getString("description"),
            rs.getDate("date_reported"),
            rs.getString("status")
        });
    }

    jTable1.setModel(model);

} catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Error loading violations: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    e.printStackTrace();
}
}

