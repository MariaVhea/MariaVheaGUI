    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
   int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a violation to delete.", "No Selection", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this violation?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    try {
        int violationId = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());

        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/mariavhea", "root", "");
        String sql = "DELETE FROM violations WHERE violation_id = ?";
        PreparedStatement pstmt = conn.prepareStatement(sql);
        pstmt.setInt(1, violationId);

        int rows = pstmt.executeUpdate();
        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Violation resolved or deleted successfully.");

            // Log the deletion
            Session sess = Session.getInstance();
            DbConnect dbc = new DbConnect();
            dbc.insertLog(sess.getUid(), "Deleted a violation record");

            // Optional: refresh the table if you have a method for that
            // loadViolationsToTable();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete violation.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        pstmt.close();
        conn.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       int selectedRow = jTable1.getSelectedRow();

    // Check if a row is selected
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a violation to mark as resolved.", "No Selection", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Get violation_id from the first column
    int violationId = (int) jTable1.getValueAt(selectedRow, 0);

    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/mariavhea", "root", "");
         PreparedStatement pstmt = conn.prepareStatement("UPDATE violations SET status = ? WHERE violation_id = ?")) {
        
        pstmt.setString(1, "Resolved");
        pstmt.setInt(2, violationId);

        int rowsUpdated = pstmt.executeUpdate();
        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(this, "Violation marked as resolved.");
            
            // Optional: update table model immediately
            jTable1.setValueAt("Resolved", selectedRow, jTable1.getColumn("Status").getModelIndex());

        } else {
            JOptionPane.showMessageDialog(this, "Failed to update status.", "Update Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

